export function mega(n: number): string {
  if (n === 0) return 'n0'
  if (n === 1) return 'n1'
  if (n === 2) return 'n2'
  if (n === 3) return 'n3'
  if (n === 4) return 'n4'
  if (n === 5) return 'n5'
  if (n === 6) return 'n6'
  if (n === 7) return 'n7'
  if (n === 8) return 'n8'
  if (n === 9) return 'n9'
  return 'other'
}
